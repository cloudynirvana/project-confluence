_default_classifier = None


def get_classifier(verbose: bool = False) -> ArchetypeMLClassifier:
    """Get or create the default ML classifier (trains on first call)."""
    global _default_classifier
    if _default_classifier is None:
        _default_classifier = ArchetypeMLClassifier(verbose=verbose)
        _default_classifier.train()
    return _default_classifier


