"""
ML Archetype Classifier — Project Confluence
==============================================

Replaces the naive centroid-distance archetype classifier with trained
Decision Tree + Random Forest ensemble classifiers. Inspired by the
disease-prediction-ml approach (binary feature matrix → ensemble classifier).

Architecture:
    1. Generate synthetic training data from ODE simulations with varied params
    2. Engineer features beyond raw 5D Φ (coupling deficit, imbalance, etc.)
    3. Train DT + RF classifiers on labeled Φ feature vectors
    4. Classify new profiles with RF probability as confidence score

Usage:
    from models.ml_classifier import ArchetypeMLClassifier
    clf = ArchetypeMLClassifier()
    clf.train()
    archetype, confidence, method = clf.classify(phi_profile)
"""

